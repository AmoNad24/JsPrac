console.log("02" > 1);  //OUTPUT: TRUE AS JS CONVERTED 2 TO A NUMBER AND THEN COMPARED 2 AND 1

console.log(null > 0); // CONVERTS null TO A NUMBER AND THEN CHECKS HENCE 0>0 IS FALSE
console.log(null == 0); // DOES NOT CONVERT TO A NUMBER hence FALSE
console.log(null >= 0); // CONVERTS null TO A NUMBER AND THEN CHECKS HENCE 0>=0 IS TRUE


// STRICT CHECKER "===" CHECKS THE DATATYPE AS WELL UNLIKE "==" WHICH CONVERTS THEN CHECKS
console.log("2" == 2);  // OUTPUT: TRUE AS IT CONVERTS "2" TO A NUMBER FIRST AND CHECKS
console.log("2" === 2); // OUTPUT: false AS THE DATATYPE OF "2" IS STRING AND THAT OF 2 is NUMBER